// 모든 FAQ에서 사용되는 태그 목록 추출 (중복 제거 및 정렬)
export const getAllTags = (): string[] => {
  const tagSet = new Set<string>();
  faqs.forEach((faq) => {
    if (faq.tags && Array.isArray(faq.tags)) {
      faq.tags.forEach((tag) => tagSet.add(tag));
    }
  });
  return Array.from(tagSet).sort();
};
